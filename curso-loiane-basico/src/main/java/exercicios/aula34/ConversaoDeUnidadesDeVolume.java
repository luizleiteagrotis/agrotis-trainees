package exercicios.aula34;

public class ConversaoDeUnidadesDeVolume {
	
	public static double converteLitrosParaCentimetros(double litros) {
		return litros*1000;
	}
	
	public static double converteMetrosParaLitros(double metros) {
		return metros*1000;
	}
	
	public static double converteMetrosParaPes(double metros) {
		return metros*35.32;
	}
	
	public static double converteGaloesParaPolegadas(double galoes) {
		return galoes*231;
	}
	
	public static double converteGaloesParaLitros(double galoes) {
		return galoes*3785;
	}
	
}
