package exercicios.aula34.exercicio6;

public class ConversaoDeUnidadesDeTempo {
	
	public static int minSegundos(int min) {
		return min * 60;
	}
	
	public static int horasMins(int horas) {
		return horas * 60;
	}
	
	public static int diasHoras(int dias) {
		return dias * 24;
	}
	
	public static int semanasDias(int semanas) {
		return semanas * 7;
	}
	
	public static int mesDias(int mes) {
		return mes * 30;
	}
	
	public static double anoDias(int ano) {
		return ano * 365.25;
	}
	
	
	
	

}
