package exercicios.aula34.exercicio5;

public class ConversaoDeUnidadeDeVolume {
	
	public static double litrosCentimetros(double valor) {
		return valor * 1000;
	}
	
	public static double metrosLitros(double valor) {
		return valor * 1000;
	}
	
	public static double metrosPes(double valor) {
		return valor * 35.32;
	}
	
	public static double galaosPolegadas(double valor) {
		return valor * 231;
	}
	
	public static double galaosLitros(double valor) {
		return valor * 3.785;
	}

}
