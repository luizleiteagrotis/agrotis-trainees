package exercicios.aula34;

public class ConversaoDeUnidadesDeArea {

	public static double converteMetroParaPes(double metro) {
		return metro * 10.76;
	}
	
	public static double convertePesParaCentimetros(double pes) {
		return pes * 929;
	}
	
	public static double converteMilhasParaAcres(double milhas) {
		return milhas*640;
	}
	
	public static double converteAcresParaPes(double acres) {
		return acres*43560;
	}
}
