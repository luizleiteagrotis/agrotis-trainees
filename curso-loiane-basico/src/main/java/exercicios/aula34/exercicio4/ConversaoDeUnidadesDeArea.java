package exercicios.aula34.exercicio4;

public class ConversaoDeUnidadesDeArea {
	
	public static double metrosPes(double valorMetros) {
		return valorMetros * 10.76;
	}
	
	public static double pesCentimetros(double valorPes) {
		return valorPes * 929;
	}
	
	public static double milhasAcres(double valorMilhas) {
		return valorMilhas * 640;
	}
	
	public static double acrePes(double valorAcres) {
		return valorAcres * 43.560;
	}
	
	
	

}
